package strings_and_dialogs;
import javax.swing.JOptionPane;

public class KnockKnock {

	public static void main(String[] args) {
		JOptionPane.showMessageDialog(null,"Knock Knock");
		JOptionPane.showMessageDialog(null, "Who's there?");
		JOptionPane.showMessageDialog(null, "A little old lady");
		JOptionPane.showMessageDialog(null, "A little old lady who?");
		JOptionPane.showMessageDialog(null, "All this time, I had no idea you could yodel");

	}

}
